** File Structure
       /your_project_directory
      |-- index.php        (Main entry point)
      |-- about.php        (About page)
      |-- contact.php      (Contact page)
      |-- other.php        (Other page)
      |-- styles.css       (CSS file for styling)

** Usage

    Place all files in a single directory.
    Start a local PHP server with the command php -S localhost:8000.
    Access the application via http://localhost:8000 in your web browser.

** Functionality

    Clicking each tab (Home, About, Contact, Other) loads the corresponding page without changing the URL structure.
    CSS styles apply to maintain a consistent look and feel across all pages.
